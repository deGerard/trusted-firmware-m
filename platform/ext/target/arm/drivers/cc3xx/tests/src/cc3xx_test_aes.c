static int run_all_aes_tests(struct aes_test_data_t *data, cc3xx_aes_keysize_t key_size,
                             cc3xx_aes_mode_t mode)
{
    int rc = 0;

#ifndef CC3XX_CONFIG_AES_GCM_VARIABLE_IV_ENABLE
    if ((data == &aes_test_gcm_long_iv || data == &aes_test_short_iv)
        && mode == CC3XX_AES_MODE_GCM) {
        return 0;
    }
#endif /* !CC3XX_CONFIG_AES_GCM_VARIABLE_IV_ENABLE */

    rc |= aes_test_lowlevel_oneshot_encrypt(data, mode, key_size);
    rc |= aes_test_lowlevel_oneshot_decrypt(data, mode, key_size);
    rc |= aes_test_lowlevel_encrypt_decrypt(data, mode, key_size);
    rc |= aes_test_lowlevel_verify_negative(data, mode, key_size);
    rc |= aes_test_lowlevel_multipart_encrypt(data, mode, key_size, 16);
    rc |= aes_test_lowlevel_multipart_decrypt(data, mode, key_size, 16);
    rc |= aes_test_lowlevel_multipart_encrypt(data, mode, key_size, 7);
    rc |= aes_test_lowlevel_multipart_decrypt(data, mode, key_size, 3);
#ifdef CC3XX_CONFIG_AES_RESTARTABLE_ENABLE
    rc |= aes_test_lowlevel_multipart_saveload_encrypt(data, mode, key_size, 16);
    rc |= aes_test_lowlevel_multipart_saveload_decrypt(data, mode, key_size, 16);
#endif /* CC3XX_CONFIG_AES_RESTARTABLE_ENABLE */
    rc |= aes_test_lowlevel_oneshot_inplace_encrypt(data, mode, key_size);
    rc |= aes_test_lowlevel_oneshot_inplace_decrypt(data, mode, key_size);

    return rc;
}

#define CREATE_AES_TESTSUITE(key_size, mode) \
static void aes_ ## key_size ## mode ## _lowlevel_tests_run(struct test_result_t *ret) \
{ \
    TEST_ASSERT(run_all_aes_tests(&aes_test_block_with_auth_data, key_size, mode) == 0, \
                "aes_test_block_with_auth_data should pass (" # key_size ", " # mode ")"); \
    if (mode != CC3XX_AES_MODE_CMAC) { \
        TEST_ASSERT(run_all_aes_tests(&aes_test_multiblock, key_size, mode) == 0, \
                    "aes_test_multiblock should pass (" # key_size ", " # mode ")"); \
    } \
    TEST_ASSERT(run_all_aes_tests(&aes_test_auth_data_only, key_size, mode) == 0, \
                "aes_test_auth_data_only should pass (" # key_size ", " # mode ")"); \
    TEST_ASSERT(run_all_aes_tests(&aes_test_short_auth_data_only, key_size, mode) == 0, \
                "aes_test_gcm_short_auth_data_only should pass (" # key_size ", " # mode ")"); \
    TEST_ASSERT(run_all_aes_tests(&aes_test_short_iv, key_size, mode) == 0, \
                "aes_test_short_iv should pass (" # key_size ", " # mode ")"); \
    TEST_ASSERT(run_all_aes_tests(&aes_test_gcm_long_iv, key_size, mode) == 0, \
                "aes_test_gcm_long_iv should pass (" # key_size ", " # mode ")"); \
    if (mode == CC3XX_AES_MODE_CCM) { \
        TEST_ASSERT(run_all_aes_tests(&aes_test_short_tag, key_size, mode) == 0, \
                    "aes_test_short_tag should pass (" # key_size ", " # mode ")"); \
    } \
    TEST_ASSERT(run_all_aes_tests(&aes_test_long_msg, key_size, mode) == 0, \
                "aes_test_gcm_long_msg should pass (" # key_size ", " # mode ")"); \
    TEST_ASSERT(run_all_aes_tests(&aes_test_long_auth_data, key_size, mode) == 0, \
                "aes_test_gcm_long_auth_data should pass (" # key_size ", " # mode ")"); \
    ret->val = TEST_PASSED; \
    return; \
} \
static struct test_t aes_ ## key_size ## mode ## _tests = { \
    &aes_ ## key_size ## mode ## _lowlevel_tests_run, \
     # key_size "_" # mode "_TEST", \
    "CC3XX aes tests (" # key_size "_" # mode ")" \
